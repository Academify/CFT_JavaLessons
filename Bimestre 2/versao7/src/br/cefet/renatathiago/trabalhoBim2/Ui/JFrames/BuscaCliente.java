    private void BotaoFiltrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoFiltrarClienteActionPerformed

        try {
            switch(FiltroClienteCBox.getSelectedIndex()){
                case 0:
                preencherTabela(cliControle.consultarTodos());
                break;
                case 1:
                preencherTabela(cliControle.consultarPorNome(PadraoFiltroClienteJTF.getText()));
                break;
                case 2:
                lista.add(cliControle.consultarPorCod(Integer.parseInt(PadraoFiltroClienteJTF.getText())));
                preencherTabela(lista);
                lista.clear();
                break;
            }
        } catch (DaoException ex) {
            Logger.getLogger(CUDCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotaoFiltrarClienteActionPerformed

