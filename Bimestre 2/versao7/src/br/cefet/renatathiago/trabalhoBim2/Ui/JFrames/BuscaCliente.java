    private void BotaoSelecionarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSelecionarClienteActionPerformed
        CUDCliente cudCliente = new CUDCliente();
        try {
            cli = cliControle.consultarPorCod((int) TabelaCliente.getValueAt(TabelaCliente.getSelectedRow(), 0));
            dispose();
            cudCliente.setVisible(true);
            cudCliente.exibirDados(cli);
            cudCliente.habilitarJTF();
            cudCliente.desabilitarBotaoCadastrar();
            cudCliente.habilitarBotaoExcluir();
            cudCliente.habilitarBotaoEditar();

        } catch (DaoException ex) {
            Logger.getLogger(CUDCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotaoSelecionarClienteActionPerformed

