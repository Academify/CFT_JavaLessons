    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        hq = new Hq();
        hq.setNmHq(tfNome.getText());
        hq.setConservacao(tfConservacao.getText());
        hq.setSinopse(tfSinopse.getText());

        try {
            hq.setPreco(Double.parseDouble(tfPreco.getText()));
            if (hq != null) {
                hqControle.inserir(hq, obra, this.uLogado);
            }
            this.setVisible(false);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Preço inválido", 0);
        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro na inserção", 0);
        } catch (ControleException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro na inserção", 0);
        }


    }//GEN-LAST:event_btCadastrarActionPerformed

