    public void preencheTabelaEndereco(List<Endereco> eList) {
        if (eList != null) {
            String[] strVet = new String[]{"Cod.", "Logradouro", "Número", "Estado", "Cidade"};
            String[][] strAMat = new String[eList.size()][5];

            for (int i = 0; i < eList.size(); i++) {
                Endereco e = eList.get(i);
                strAMat[i][0] = e.getCdEndereco() + "";
                strAMat[i][1] = e.getLogradouro();
                strAMat[i][2] = e.getNumero() + "";
                strAMat[i][3] = e.getEstado();
                strAMat[i][4] = e.getCidade();
            }

            tbEndereco.setModel(new DefaultTableModel(
                    strAMat, strVet));
        }
        desabilitarBotoes();
    }

    private void consultaPorNome() {
        List<Endereco> eList = null;
        try {
            eList = eControle.consultaPorLogradouro(uLogado, tfLogradouro.getText());
        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro no BD ", HEIGHT);
        }
        preencheTabelaEndereco(eList);
    }

    public Endereco getEnderecoFromTable() {
        Endereco endRet = null;

        int linha = tbEndereco.getSelectedRow();

        if (linha >= 0) {
            TableModel m = tbEndereco.getModel();
            String idStr = m.getValueAt(linha, 0).toString();
            int id = Integer.parseInt(idStr);

            endRet = new Endereco();
            endRet.setCdEndereco(id);
            endRet.setLogradouro(m.getValueAt(linha, 1).toString());
            String numero = m.getValueAt(linha, 2).toString();
            endRet.setNumero(Integer.parseInt(numero));
            endRet.setEstado(m.getValueAt(linha, 3).toString());
            endRet.setCidade(m.getValueAt(linha, 4).toString());
        }
        return endRet;
    }

    private void desabilitarBotoes() {
        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);
        btVisualizar.setEnabled(false);
    }

