    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir", "Exclusão", WIDTH);
        List<Endereco> eList = null;

        if (opt == JOptionPane.OK_OPTION) {
            Endereco end = getEnderecoFromTable();
            try {
                eControle.excluir(end.getCdEndereco());

                int linha = tbEndereco.getSelectedRow();
                DefaultTableModel m = (DefaultTableModel) tbEndereco.getModel();
                m.removeRow(linha);
                desabilitarBotoes();

            } catch (DaoException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro no BD ", HEIGHT);
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

