    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        String senha = tfSenha.getText();
        String email = tfEmail.getText();
        if (senha.equals(tfConfirmarSenha.getText()) && email.equals(tfConfirmarEmail.getText())) {
            try {               
                usuario.setCpf(tfCpf.getText());
                usuario.setEmail(tfEmail.getText());
                usuario.setNmUsuario(tfNome.getText());
                usuario.setRg(tfRg.getText());
                usuario.setSenha(tfSenha.getText());
                usuario.setTelefone(tfTelefone.getText());

                uControle.alterar(usuario);
                
                this.setVisible(false);

            } catch (DaoException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro na alteração", HEIGHT);
            } catch (ControleException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro na alteração", HEIGHT);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Os dados de login não foram confirmados! Tente novamente", "Erro na alteração", 1);
        }

    }//GEN-LAST:event_btAlterarActionPerformed

