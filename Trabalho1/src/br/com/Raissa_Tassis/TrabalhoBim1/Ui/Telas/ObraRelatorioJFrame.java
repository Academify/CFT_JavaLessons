    private void btPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaActionPerformed

        try{
            String nome = tfNome.getText();
            int ano1 = Integer.parseInt(tfLancamento1.getText());
            int ano2 = Integer.parseInt(tfLancamento2.getText());
            
            if(ano1 >= 0 && ano2 >= 0){
                
                obras = oControle.consultaRelatorio(nome, ano1, ano2);
                preencheTabela(obras);        
                btEmitir.setEnabled(true);
            } else{
                JOptionPane.showMessageDialog(this, "Preço negativo não vale" ,"Preço inválido!", HEIGHT);
            }
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Preço inválido!", HEIGHT);
        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro na consulta!", HEIGHT);
        }
        
        
                
    }//GEN-LAST:event_btPesquisaActionPerformed

