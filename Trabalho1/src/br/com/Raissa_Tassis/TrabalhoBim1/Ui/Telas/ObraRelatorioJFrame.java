    public Hq getHqFromTable() {
        Hq hqRet = null;

        int linha = tbObra.getSelectedRow();

        if (linha >= 0) {
            TableModel m = tbObra.getModel();
            String idStr = m.getValueAt(linha, 0).toString();
            int id = Integer.parseInt(idStr);

            hqRet = new Hq();
            hqRet.setCdHq(id);
            hqRet.setNmHq(m.getValueAt(linha, 1).toString());
            hqRet.setPreco(parseDouble(m.getValueAt(linha, 2).toString()));
            hqRet.setConservacao(m.getValueAt(linha, 3).toString());
        }
        return hqRet;
    }

    public void preencheTabela(List<Obra> oList) {
        if (oList != null) {
            String[] strVet = new String[]{"Cod.", "Nome", "Lançamento"};
            String[][] strAMat = new String[oList.size()][3];

            for (int i = 0; i < oList.size(); i++) {
                Obra o = oList.get(i);
                strAMat[i][0] = o.getCdObra()+ "";
                strAMat[i][1] = o.getNmObra();
                strAMat[i][2] = o.getDtLancamento() + "";
            }

            tbObra.setModel(new DefaultTableModel(
                    strAMat, strVet));
        }

        desabilitarBotoes();
    }

    private void desabilitarBotoes() {
        btEmitir.setEnabled(false);
    }

