    private void preencheTabela(List<Usuario> uList) {
        if (uList != null) {
            String[] strVet = new String[]{"Cod.", "Nome", "Email", "CPF"};
            String[][] strAMat = new String[uList.size()][4];

            for (int i = 0; i < uList.size(); i++) {
                Usuario user = uList.get(i);
                strAMat[i][0] = user.getCdUsuario() + "";
                strAMat[i][1] = user.getNmUsuario();
                strAMat[i][2] = user.getEmail();
                strAMat[i][3] = user.getCpf();
            }

            jTableUsuario.setModel(new DefaultTableModel(
                    strAMat, strVet));
        }       
    }

    private Usuario getUsuarioFromTable() {
        Usuario uRet = null;

        int linha = jTableUsuario.getSelectedRow();

        if (linha >= 0) {
            TableModel m = jTableUsuario.getModel();
            String idStr = m.getValueAt(linha, 0).toString();
            int id = Integer.parseInt(idStr);

            uRet = new Usuario();
            uRet.setCdUsuario(id);
            uRet.setNmUsuario(m.getValueAt(linha, 1).toString());
            uRet.setEmail(m.getValueAt(linha, 2).toString());
            uRet.setCpf(m.getValueAt(linha, 3).toString());
        }
        return uRet;
    }

    private void consultaPorNome() {
        List<Usuario> uList = null;
        try {
            uList = uControle.consultaPorNome(tfNome.getText());
        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro no BD ", HEIGHT);
        }
        preencheTabela(uList);
    }

